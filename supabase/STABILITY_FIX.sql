-- Upgrade process_stock_count to handle notes correctly
CREATE OR REPLACE FUNCTION public.process_stock_count(
  p_location_id uuid,
  p_created_by uuid,
  p_notes text,
  p_items jsonb
)
RETURNS uuid
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_count_id uuid;
  v_item jsonb;
  v_product_id uuid;
  v_system_qty integer;
  v_counted_qty integer;
  v_final_qty integer;
  v_diff integer;
  v_adjustment_mode public.adjustment_type;
  v_business_id uuid;
BEGIN
  -- Get business_id from location
  SELECT business_id INTO v_business_id FROM public.locations WHERE id = p_location_id;

  -- Create master record
  INSERT INTO public.stock_counts (
    business_id, stock_name, location_id, created_by, notes
  ) VALUES (
    v_business_id, 'Adj - ' || TO_CHAR(NOW(), 'YYYY-MM-DD HH24:MI'), p_location_id, p_created_by, NULLIF(p_notes, '')
  ) RETURNING id INTO v_count_id;

  FOR v_item IN SELECT * FROM jsonb_array_elements(p_items)
  LOOP
    v_product_id      := (v_item ->> 'product_id')::uuid;
    v_counted_qty     := (v_item ->> 'counted_quantity')::integer;
    v_adjustment_mode  := (v_item ->> 'adjustment_mode')::public.adjustment_type;
    
    -- Get current stock
    SELECT quantity INTO v_system_qty FROM public.product_stocks WHERE product_id = v_product_id AND location_id = p_location_id;
    v_system_qty := COALESCE(v_system_qty, 0);

    -- Calculate delta
    IF v_adjustment_mode = 'add' THEN
      v_final_qty := v_system_qty + v_counted_qty;
      v_diff := v_counted_qty;
    ELSE
      v_final_qty := v_system_qty - v_counted_qty;
      v_diff := -v_counted_qty;
    END IF;

    -- Update records
    INSERT INTO public.stock_count_items (
      business_id, stock_count_id, product_id, system_quantity, adjustment_mode, counted_quantity, final_quantity
    ) VALUES (
      v_business_id, v_count_id, v_product_id, v_system_qty, v_adjustment_mode, v_counted_qty, v_final_qty
    );

    INSERT INTO public.product_stocks (business_id, product_id, location_id, quantity)
    VALUES (v_business_id, v_product_id, p_location_id, v_final_qty)
    ON CONFLICT (product_id, location_id) DO UPDATE SET quantity = EXCLUDED.quantity;

    -- Update Global Total
    UPDATE public.products p
    SET stock_quantity = (SELECT COALESCE(SUM(quantity), 0) FROM public.product_stocks WHERE product_id = p.id)
    WHERE id = v_product_id AND business_id = v_business_id;

    -- Record Movement (NOW INCLUDES NOTES COLUMN)
    IF v_diff != 0 THEN
       INSERT INTO public.stock_movements (
        business_id, product_id, user_id, movement_type, quantity, location_id, reference_type, reference_id, notes
      ) VALUES (
        v_business_id, v_product_id, p_created_by, 'count', v_diff, p_location_id, 'stock_count', v_count_id, COALESCE(p_notes, 'Standard Adjustment')
      );
    END IF;
  END LOOP;

  RETURN v_count_id;
END;
$$;

